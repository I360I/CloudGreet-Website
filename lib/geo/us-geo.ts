/** Resolve a US location from whatever fields we have. Null when unknown. */
export function geolocate({
  state, phone,
}: {
  city?: string | null
  state?: string | null
  phone?: string | null
}): [number, number] | null {
  if (state) {
    const key = state.trim().length === 2
      ? state.trim().toUpperCase()
      : STATE_NAMES[state.trim().toLowerCase()]
    const c = key ? STATE_CENTROIDS[key] : undefined
    if (c) return c
  }
  if (phone) {
    const digits = phone.replace(/\D/g, '')
    const code = digits.length === 11 && digits.startsWith('1')
      ? digits.slice(1, 4)
      : digits.slice(0, 3)
    const c = AREA_CODES[code]
    if (c) return c
  }
  return null
}

/** Deterministic small jitter so same-metro dots don't stack exactly. */
export function jitter(lat: number, lng: number, seed: string): [number, number] {
  let h = 0
  for (let i = 0; i < seed.length; i++) h = (h * 31 + seed.charCodeAt(i)) >>> 0
  const a = ((h % 360) / 360) * Math.PI * 2
  const r = 0.12 + ((h >>> 9) % 100) / 100 * 0.18
  return [lat + Math.sin(a) * r, lng + Math.cos(a) * r]
}
